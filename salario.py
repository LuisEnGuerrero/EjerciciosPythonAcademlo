# Programa para calcular el Sueldo por semana.
print('Calculadora de Sualdos')
horas = int(input('\nIngresa la cantidad de horas trabajadas en la semana: '))
valorHora = float(input('Ingresa el valor de tu hora de trabajo: '))
salario=horas*valorHora
print('\nTe corresponde como Salario la suma de:',salario)
